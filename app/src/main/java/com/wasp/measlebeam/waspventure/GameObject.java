package com.wasp.measlebeam.waspventure;

import android.graphics.Rect;

/**
 * Abstract class GameObject will be used as template (be extended by) all moving objects in the
 * game.
 *
 * Created by Viktor Stärn on 2015-08-30.
 */
public abstract class GameObject {
    protected int x;
    protected int y;
    protected int dx;                           //rate of object x-position change
    protected int dy;                           //rate of object y-position change
    protected int width;
    protected int height;

    public void setX(int x)
    {
        this.x = x;
    }

    public void setY(int y)
    {
        this.y = y;
    }

    public int getX()
    {
        return this.x;
    }

    public int getY()
    {
        return this.y;
    }

    public int getWidth()
    {
        return this.width;
    }

    public int getHeight()
    {
        return this.height;
    }

    public Rect getRectangle()
    {
        return new Rect(x, y, x+width, y+height);
    }


}
